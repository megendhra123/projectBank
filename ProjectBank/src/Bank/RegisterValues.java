package Bank;

public class RegisterValues {
	String RAcname;
	String RAcNo;
	int rPinNo;
	
	public void setRAcname(String rAcname) {
		RAcname = rAcname;
	}
	public String getRAcname() {
		return RAcname;
	}
	
	public void setRAcNo(String rAcNo) {
		RAcNo = rAcNo;
	}
	public String getRAcNo() {
		return RAcNo;
	}
	
	public void setrPinNo(int rPinNo) {
		this.rPinNo = rPinNo;
	}
	public int getrPinNo() {
		return rPinNo;
	}

}
