package Bank;

public class LoginValues {
	String acNo;
	int PinNo;
	
	
	public void setAcNo(String acNo) {
		this.acNo = acNo;
	}
	public String getAcNo() {
		return this.acNo;
	}
	
	public void setPinNo(int pinNo) {
		PinNo = pinNo;
	}
	public int getPinNo() {
		return PinNo;
	}
	

}
